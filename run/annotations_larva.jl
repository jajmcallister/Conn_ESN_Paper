# Function to compute per-subnetwork, per-task subtype ranking
function per_subnet_ranking(coarse_types_conn, weighted_tv_conn_adult)
    n_subnets = length(coarse_types_conn)
    n_tasks = length(weighted_tv_conn_adult[1])  # 8 tasks
    rankings = Vector{Any}(undef, n_subnets)

    for sid in 1:n_subnets
        types = coarse_types_conn[sid]
        task_data = weighted_tv_conn_adult[sid]  # 8 tasks × no. instantiations × neurons
        subnet_rankings = Vector{Any}(undef, n_tasks)

        for tid in 1:n_tasks
            # collect all neurons across no. instantiations for this task
            all_neuron_vals = reduce(vcat, task_data[tid])  # Vector of length (30*neurons)
            inn = length(weighted_tv_conn_adult[sid][tid])
            all_types = repeat(types, inner=inn)  # repeat type labels to match flattened neuron vals

            # group by type and take mean per neuron
            type_scores = Dict{String, Float64}()
            for t in unique(all_types)
                v = all_neuron_vals[all_types .== t]
                type_scores[t] = mean(v)  # average per neuron
            end

            # rank subtypes by descending score
            # sorted_types = sort(collect(keys(type_scores)), by=t -> type_scores[t], rev=true)
            # or alphabetical
            sorted_types = sort(collect(keys(type_scores)))  
            subnet_rankings[tid] = (sorted_types, type_scores)
        end

        rankings[sid] = subnet_rankings
    end

    return rankings
end

function subnet_celltype_importance(coarse_types_conn, weighted_tv_conn_adult)
    n_subnets = length(coarse_types_conn)
    n_tasks = length(weighted_tv_conn_adult[1])
    subnet_matrices = Vector{Any}(undef, n_subnets)

    for sid in 1:n_subnets
        types = coarse_types_conn[sid]          # celltypes for this subnetwork
        task_data = weighted_tv_conn_adult[sid] # 8 tasks × 30 instantiations × neurons
        celltypes = unique(types)
        n_types = length(celltypes)

        # matrix: tasks × celltypes
        mat = zeros(n_tasks, n_types)

        for tid in 1:n_tasks
            inst_arrays = task_data[tid]                  # 30 arrays of length n_neurons
            all_neuron_vals = reduce(vcat, inst_arrays)  # flatten to length n_neurons * 30
            n_inst = length(inst_arrays)
            all_types = repeat(types, inner=n_inst)      # repeat neuron types per instantiation

            for (j, ct) in enumerate(celltypes)
                mat[tid, j] = mean(all_neuron_vals[all_types .== ct])
            end
        end

        subnet_matrices[sid] = (celltypes, mat)
    end

    return subnet_matrices
end


